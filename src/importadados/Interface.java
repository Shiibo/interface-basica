    private void buscarLabelSolta(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarLabelSolta
        this.display.setText("");
        
        for (int i = 0; i < bd.size(); i++){
            if(bd.get(i).existe(this.buscarLabel.getText()))
                this.display.append(bd.get(i).getNome()+"\n");
        }
        this.contTotal.setText(String.valueOf(this.display.getLineCount()-1)+" registros encontrados");
    }//GEN-LAST:event_buscarLabelSolta

    private void displayClick(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_displayClick
        try {
            // descubro qual linha foi clicada
            int linha = this.display.getLineOfOffset(display.getCaretPosition());
            // defino o inicio do texto dessa linha
            int inicio = display.getLineStartOffset(linha);
            // defino o final da linha
            int fim = display.getLineEndOffset(linha);
            
            //gero um string a partir a linha que foi clicada
            String s = display.getDocument().getText(inicio, fim-inicio-1);
            this.display.select(inicio, fim-1);
            
            for (int i=0; i < bd.size(); i++){
                if(bd.get(i).existe(s)){
                    cont = i;
                    this.Atualiza();
                }
            }
        } catch(BadLocationException ex) {
            Logger.getLogger(Interface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_displayClick

    public void Atualiza(){
        if(bd.size()<=0){
            this.dispNome.setText("Nada a exibir");
            this.dispCPF.setText("Nada a exibir");
            this.dispData.setText("Nada a exibir");
            this.dispEnd.setText("Nada a exibir");
            this.dispNum.setText("Nada a exibir");
            this.dispCidade.setText("Nada a exibir");
            this.dispEstado.setText("Nada a exibir");
        } else{
        
            if(cont > bd.size()-1)
                cont = bd.size()-1;

            this.dispNome.setText(this.bd.get(cont).getNome());
            this.dispCPF.setText(this.bd.get(cont).getCpf());
            this.dispData.setText(this.bd.get(cont).getData_nasc());
            this.dispEnd.setText(this.bd.get(cont).getEndereco());
            this.dispNum.setText(String.valueOf(this.bd.get(cont).getNumero()));
            this.dispCidade.setText(this.bd.get(cont).getCidade());
            this.dispEstado.setText(this.bd.get(cont).getEstado());
        }
    }
